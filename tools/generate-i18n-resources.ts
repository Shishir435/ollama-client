import fs from "fs"
import path from "path"
import { fileURLToPath } from "url"

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)

const LOCALES_DIR = path.join(__dirname, "../src/locales")
const OUTPUT_FILE = path.join(__dirname, "../src/i18n/resources.ts")
const LITE_OUTPUT_FILE = path.join(__dirname, "../src/contents/i18n-lite.ts")

async function generateResources() {
  console.log("📦 Generating i18n resources...")

  const resources: Record<string, any> = {}
  const liteResources: Record<string, any> = {}
  
  if (!fs.existsSync(LOCALES_DIR)) {
    console.error(`❌ Locales directory not found: ${LOCALES_DIR}`)
    process.exit(1)
  }

  const locales = fs.readdirSync(LOCALES_DIR).filter(item => {
    return fs.statSync(path.join(LOCALES_DIR, item)).isDirectory()
  })

  for (const locale of locales) {
    const translationFile = path.join(LOCALES_DIR, locale, "translation.json")
    
    if (fs.existsSync(translationFile)) {
      try {
        const content = fs.readFileSync(translationFile, "utf-8")
        const json = JSON.parse(content)
        resources[locale] = {
          translation: json
        }

        // Extract lite resources (only selection_button keys)
        if (json.selection_button) {
           const flat: Record<string, string> = {}
           for (const [key, value] of Object.entries(json.selection_button)) {
             flat[`selection_button.${key}`] = value as string
           }
           liteResources[locale] = flat
        }

        console.log(`  ✓ Loaded ${locale}`)
      } catch (error) {
        console.error(`  ❌ Error loading ${locale}:`, error)
      }
    }
  }

  // Generate full resources
  const fileContent = `// This file is auto-generated by tools/generate-i18n-resources.ts
// Do not edit this file directly

export const resources = ${JSON.stringify(resources, null, 2)} as const;
`

  fs.writeFileSync(OUTPUT_FILE, fileContent)
  console.log(`✨ Generated resources at ${OUTPUT_FILE}`)

  // Generate lite i18n
  const liteContent = `// This file is auto-generated by tools/generate-i18n-resources.ts
// Do not edit this file directly

import { useStorage } from "@plasmohq/storage/hook"
import { STORAGE_KEYS } from "@/lib/constants"
import { plasmoGlobalStorage } from "@/lib/plasmo-global-storage"

const translations: Record<string, Record<string, string>> = ${JSON.stringify(liteResources, null, 2)}

export const useLiteTranslation = () => {
  const [lang] = useStorage<string>({
    key: STORAGE_KEYS.LANGUAGE,
    instance: plasmoGlobalStorage
  })

  const t = (key: string) => {
    const language = lang || "en"
    let targetLang = language
    
    // Simple fallback logic
    if (!translations[targetLang]) {
      if (typeof targetLang === 'string') {
          if (targetLang.startsWith("zh")) targetLang = "zh"
          else if (targetLang.startsWith("fr")) targetLang = "fr"
          else targetLang = "en"
      } else {
          targetLang = "en"
      }
    }
    
    // Safety check if language still not found
    if (!translations[targetLang]) {
        targetLang = "en"
    }

    return translations[targetLang]?.[key] || translations.en?.[key] || key
  }

  return { t }
}
`
  fs.writeFileSync(LITE_OUTPUT_FILE, liteContent)
  console.log(`✨ Generated lite i18n at ${LITE_OUTPUT_FILE}`)

  try {
    console.log("💅 Formatting with Biome...")
    const { execSync } = await import("child_process")
    execSync(`npx biome check --write "${OUTPUT_FILE}" "${LITE_OUTPUT_FILE}"`, { stdio: "inherit" })
    console.log("✅ Formatting complete")
  } catch (error) {
    console.error("⚠️ Failed to format generated file:", error)
  }
}

generateResources()
